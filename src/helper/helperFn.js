//plus
function plusFn(num1, num2) {
  return num1 + num2;
}

//minus
function minusFn(num1, num2) {
  return num1 - num2;
}
//multiply
function multiplyFn(num1, num2) {
  return num1 * num2;
}

//divide
function divideFn(num1, num2) {
  return (num1 / num2).toFixed(3);
}
export { plusFn, minusFn, multiplyFn, divideFn };
